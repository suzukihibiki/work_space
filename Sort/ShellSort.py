def shell_sort(l):
    
    G = list([int((2.25**i-1)/1.25) for i in range(17, 0, -1)])
    G = [x for x in G if x <= len(l)]
    return G, l

test_data = [3,2,1,8,9]
G, l = shell_sort(
    l = test_data
)
print(G)
print(l)